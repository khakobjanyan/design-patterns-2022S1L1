public class Entity {
    private int instanceId;
    private int id;
    private  String name;

    public int getInstanceId() {
        return instanceId;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setInstanceId(int instanceId) {
        this.instanceId = instanceId;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
